<script setup lang="ts">
import { computed, ref, watch } from 'vue'
import type { RcaDocument } from '@/types/ticket'

const props = withDefaults(
  defineProps<{
    rca: RcaDocument
    canManage: boolean
    canNotify: boolean
    /** A save/send is in flight — both actions lock so nothing sends twice. */
    isBusy?: boolean
  }>(),
  { isBusy: false },
)

const emit = defineEmits<{
  (e: 'save-draft', customerSummary: string): void
  (e: 'send-customer', customerSummary: string): void
}>()

const customerDraft = ref(props.rca.customer_summary || '')
watch(
  () => props.rca.customer_summary,
  (next) => {
    customerDraft.value = next || ''
  },
)

interface Section {
  id: string
  label: string
  present: boolean
}

const sections = computed<Section[]>(() => [
  { id: 'rca-summary', label: 'Summary', present: !!props.rca.summary },
  { id: 'rca-impact', label: 'Impact', present: !!props.rca.impact },
  { id: 'rca-timeline', label: 'Timeline', present: !!props.rca.timeline?.length },
  { id: 'rca-log', label: 'Investigation log', present: !!props.rca.investigation_log },
  { id: 'rca-factors', label: 'Contributing factors', present: !!props.rca.contributing_factors?.length },
  { id: 'rca-conclusion', label: 'Conclusion', present: !!props.rca.conclusion },
  { id: 'rca-remediation', label: 'Remediation', present: !!props.rca.remediation },
  { id: 'rca-prevention', label: 'Prevention', present: !!props.rca.prevention },
  { id: 'rca-customer', label: 'Customer summary', present: true },
])

function jumpTo(id: string) {
  document.getElementById(id)?.scrollIntoView({ behavior: 'smooth', block: 'start' })
}
</script>

<template>
  <div class="rca-doc">
    <div class="rca-head">
      <div>
        <div class="rca-title">Root-cause analysis</div>
        <div class="rca-byline">
          Generated by ChatterMate AI
          <template v-if="rca.reviewed_by_name"> · reviewed by {{ rca.reviewed_by_name }}</template>
          <template v-else> · not yet reviewed</template>
          <span class="version mono">v{{ rca.version }}</span>
          <span v-if="rca.is_partial" class="partial-tag">Partial — budget reached</span>
        </div>
      </div>
      <div v-if="rca.confidence != null" class="rca-confidence mono">
        confidence {{ rca.confidence.toFixed(2) }}
      </div>
    </div>

    <div class="rca-grid">
      <nav class="rca-toc">
        <button
          v-for="section in sections.filter((s) => s.present)"
          :key="section.id"
          class="toc-link"
          @click="jumpTo(section.id)"
        >
          {{ section.label }}
        </button>
      </nav>

      <div class="rca-body">
        <section v-if="rca.summary" id="rca-summary">
          <h4 class="section-label">Summary</h4>
          <p class="section-text">{{ rca.summary }}</p>
        </section>

        <section v-if="rca.impact" id="rca-impact">
          <h4 class="section-label">Impact</h4>
          <p class="section-text">{{ rca.impact }}</p>
        </section>

        <section v-if="rca.timeline?.length" id="rca-timeline">
          <h4 class="section-label">Timeline</h4>
          <div class="timeline">
            <div v-for="(entry, i) in rca.timeline" :key="i" class="timeline-row">
              <span class="timeline-time mono">{{ entry.time }}</span>
              <span class="timeline-event">{{ entry.event }}</span>
            </div>
          </div>
        </section>

        <section v-if="rca.investigation_log" id="rca-log">
          <h4 class="section-label">Investigation log</h4>
          <p class="section-text">{{ rca.investigation_log }}</p>
        </section>

        <section v-if="rca.contributing_factors?.length" id="rca-factors">
          <h4 class="section-label">Contributing factors</h4>
          <ul class="factor-list">
            <li v-for="(factor, i) in rca.contributing_factors" :key="i">{{ factor }}</li>
          </ul>
        </section>

        <section v-if="rca.conclusion" id="rca-conclusion">
          <h4 class="section-label">Conclusion</h4>
          <p class="section-text conclusion">{{ rca.conclusion }}</p>
        </section>

        <section v-if="rca.remediation" id="rca-remediation">
          <h4 class="section-label">Remediation</h4>
          <p class="section-text">{{ rca.remediation }}</p>
        </section>

        <section v-if="rca.prevention" id="rca-prevention">
          <h4 class="section-label">Prevention</h4>
          <p class="section-text">{{ rca.prevention }}</p>
        </section>

        <section id="rca-customer">
          <h4 class="section-label">Customer summary</h4>
          <div class="customer-card">
            <textarea
              v-if="canManage"
              v-model="customerDraft"
              class="customer-editor"
              placeholder="Plain-language summary for the customer…"
            ></textarea>
            <p v-else class="section-text">{{ rca.customer_summary || '—' }}</p>
            <div v-if="canManage" class="customer-actions">
              <span v-if="!canNotify" class="no-channel-note">
                No customer channel — link a conversation or add a customer email to send.
              </span>
              <button
                class="draft-btn"
                :disabled="props.isBusy"
                @click="emit('save-draft', customerDraft)"
              >
                Save draft
              </button>
              <button
                class="send-btn"
                :disabled="props.isBusy || !canNotify || !customerDraft.trim()"
                @click="emit('send-customer', customerDraft)"
              >
                {{ props.isBusy ? 'Sending…' : 'Send to customer' }}
              </button>
            </div>
          </div>
        </section>
      </div>
    </div>
  </div>
</template>

